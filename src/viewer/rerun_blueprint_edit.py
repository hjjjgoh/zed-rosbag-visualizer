import rerun as rr
import rerun.blueprint as rrb
import numpy as np


# set rerun blueprint
def setup_rerun_blueprint():
    blueprint = rrb.Horizontal(
        rrb.Vertical(
            # 3D 뷰를 탭으로 분리: All / Points
            rrb.Tabs(
                rrb.Spatial3DView(
                    name="3D (All)",
                    origin="world",
                    #contents= "world",
                ),
            ),
            rrb.TextDocumentView(
                name="Description", 
                origin="/description"
                ),
            row_shares=[7, 3],
        ),
        rrb.Vertical(
            rrb.Spatial2DView(
                name="RGB",
                origin="world/camera/image",
                contents="world/camera/image/rgb",
            ),
            rrb.Spatial2DView(
                    name="Depth_masked",
                    origin="world/camera/image",
                    contents="world/camera/image/depth_foundation",
                ),
            rrb.Tabs(
                rrb.Spatial2DView(
                    name="Depth_fondationstereo",
                    origin="image",
                    contents="image/depth",
                ),
                
            ),
            name="2D Views",
            row_shares=[3, 3],
        ),
        column_shares=[3, 1],
    )
    rr.send_blueprint(blueprint)
    rr.log("world", rr.ViewCoordinates.RDF, static=True)
    # R: Right, D: Down, F: Forward / [meter]
   

# log description to rerun
def log_description():
    description = """
    ### ZED ROS Bag Visualizer

    This viewer displays the pre-processed output from the `preprocess_edit.py` script.

    - **3D View**:
      - Shows the camera's trajectory.
      - Displays up to two types of point clouds:
        - **Depth-based Point Cloud**: Automatically generated by Rerun from the processed Depth video (`depth_foundation.mp4` or `depth_zed.mp4`). Can be toggled via `world/camera/image/depth_foundation`.
        - **Topic-based Point Cloud**: Loaded from `pointcloud.json` if available. This represents the original point cloud from the ZED camera topic. Can be toggled via `world/camera/pointcloud_topic`.

    - **2D Views**:
      - **RGB**: The main RGB video feed.
      - **Depthmap**: A colorized representation of the depth video for visualization.
    """
    rr.log("description", rr.TextDocument(description, media_type=rr.MediaType.MARKDOWN), static=True)
